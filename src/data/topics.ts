/**
 * 根据ID获取议题
 */
export function getTopicById(id: number): Topic | undefined {
  return topics.find(t => t.id === id);
}

/**
 * 根据阶段获取议题列表
 */
export function getTopicsByStage(stage: Stage): Topic[] {
  return topics.filter(t => t.stage === stage);
}
